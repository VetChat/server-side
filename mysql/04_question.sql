(
    question_id     INT AUTO_INCREMENT,
    question_set_id INT          NOT NULL,
    question        VARCHAR(255) NOT NULL,
    pattern         ENUM ('choice', 'duration', 'text'),
    image_path      VARCHAR(1000),
    ordinal         INT          NOT NULL,
